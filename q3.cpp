class Solution{
  public:
    int countSubArrayProductLessThanK(const vector<int>& a, int n, long long k) {
        int ans=0;
        long long p=1;
        for(int st=0,ed=0;ed<n;ed++){
            p*=a[ed];
            while(st<ed && p >=k){
                p/=a[st++];
            }
            if(p<k){
                int len=ed-st+1;
                ans+=len;
            }
        }
        return ans;
    }
};

